import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    cur1 = l1
    cur2 = l2
    lst1 = []
    lst2 = []
    total = 0
    string1 = add_value_into_lst(cur1, lst1)
    string2 = add_value_into_lst(cur2, lst2)
    total = int(string1)+int(string2)
    total = str(total)
    l3 = ListNode(0, None)
    cur3 = l3
    for i in range(len(total)-1, -1, -1):
        cur3.next = ListNode(total[i], None)
        cur3 = cur3.next
    return l3.next


def add_value_into_lst(cur, lst):
    ans = ""
    while cur is not None:
        x = cur.val
        lst.append(x)
        cur = cur.next
    for i in range(len(lst)):
        ans += str(lst.pop())
    return ans


